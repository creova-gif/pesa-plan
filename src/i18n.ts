/** Sync i18next language with AppState. Also sets RTL direction for Arabic. */
export function syncI18nLanguage(lang: 'en' | 'sw' | 'fr' | 'ar' | 'pt'): void {
  if (i18n.language !== lang) i18n.changeLanguage(lang);
  document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
  document.documentElement.lang = lang;
}
